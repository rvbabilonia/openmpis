package org.vincenzolabs.openmpis.enumeration;

/**
 * The enumeration of eye colors.
 *
 * @author Rey Vincent Babilonia
 */
public enum EyeColor {

    /**
     * Unknown.
     */
    UNKNOWN,
    /**
     * Amber, yellow or golden.
     */
    AMBER,
    /**
     * Blue.
     */
    BLUE,
    /**
     * Brown.
     */
    BROWN,
    /**
     * Gray.
     */
    GRAY,
    /**
     * Green.
     */
    GREEN,
    /**
     * Hazel (brown and green)
     */
    HAZEL,
    /**
     * Red or violet (albino).
     */
    RED,
    /**
     * Other.
     */
    OTHER
}
