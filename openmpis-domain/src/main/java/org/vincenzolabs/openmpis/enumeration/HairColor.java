package org.vincenzolabs.openmpis.enumeration;

/**
 * The enumeration of hair colors.
 *
 * @author Rey Vincent Babilonia
 */
public enum HairColor {

    /**
     * Unknown.
     */
    UNKNOWN,
    /**
     * Light to dark red-brown.
     */
    AUBURN,
    /**
     * Black.
     */
    BLACK,
    /**
     * Blond.
     */
    BLOND,
    /**
     * Brown, brunette or chestnut.
     */
    BROWN,
    /**
     * Gray.
     */
    GRAY,
    /**
     * Light strawberry blond shades, titian, copper or completely red
     */
    RED,
    /**
     * White.
     */
    WHITE,
    /**
     * Other.
     */
    OTHER
}
