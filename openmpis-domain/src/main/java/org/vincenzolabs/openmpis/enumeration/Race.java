package org.vincenzolabs.openmpis.enumeration;

/**
 * The enumeration of races as defined by the US Census
 * (https://en.wikipedia.org/wiki/Race_and_ethnicity_in_the_United_States_Census).
 *
 * @author Rey Vincent Babilonia
 */
public enum Race {

    /**
     * Unknown.
     */
    UNKNOWN,
    /**
     * Caucasian.
     */
    WHITE,
    /**
     * Black e.g. African and African-American.
     */
    BLACK,
    /**
     * Native American.
     */
    AMERICAN_INDIAN,
    /**
     * Asian Indian.
     */
    ASIAN_INDIAN,
    /**
     * Chinese.
     */
    CHINESE,
    /**
     * Japanese.
     */
    JAPANESE,
    /**
     * Karean.
     */
    KOREAN,
    /**
     * Filipino.
     */
    FILIPINO,
    /**
     * Vietnamese.
     */
    VIETNAMESE,
    /**
     * Other Asian e.g. Singaporean and Malaysian.
     */
    OTHER_ASIAN,
    /**
     * Native Hawaiian.
     */
    NATIVE_HAWAIIAN,
    /**
     * Guamanian.
     */
    GUAMANIAN,
    /**
     * Samoan.
     */
    SAMOAN,
    /**
     * Other Pacific islander e.g. Fijian and Tongan
     */
    OTHER_PACIFIC_ISLANDER,
    /**
     * Other.
     */
    OTHER
}
